package com.example.ejemjson.model;

public class JSONObjeto {
	private String id;
	private String title;
	private String excerpt;
	private String urlImgThumbnail;
	private String urlImgFull;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getExcerpt() {
		return excerpt;
	}

	public void setExcerpt(String excerpt) {
		this.excerpt = excerpt;
	}

	public String getUrlImgThumbnail() {
		return urlImgThumbnail;
	}

	public void setUrlImgThumbnail(String urlImgThumbnail) {
		this.urlImgThumbnail = urlImgThumbnail;
	}

	public String getUrlImgFull() {
		return urlImgFull;
	}

	public void setUrlImgFull(String urlImgFull) {
		this.urlImgFull = urlImgFull;
	}

}
